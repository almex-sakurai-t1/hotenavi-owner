package jp.happyhotel.common;

public class HappyHotelBasicInfo
{

    private String strHotelId = null;
    private String strName    = null;

    /**
     * @return the strHotelId
     */
    public String getStrHotelId()
    {
        return strHotelId;
    }

    /**
     * @param strHotelId the strHotelId to set
     */
    public void setStrHotelId(String strHotelId)
    {
        this.strHotelId = strHotelId;
    }

    /**
     * @return the strName
     */
    public String getStrName()
    {
        return strName;
    }

    /**
     * @param strName the strName to set
     */
    public void setStrName(String strName)
    {
        this.strName = strName;
    }

}
